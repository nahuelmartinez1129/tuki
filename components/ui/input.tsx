import * as React from "react";

import { cn } from "@/lib/utils";

export type InputProps = React.InputHTMLAttributes<HTMLInputElement>;

const Input = React.forwardRef<HTMLInputElement, InputProps>(
  ({ className, type, ...props }, ref) => {
    return (
      <input
        type={type}
        className={cn(
          "flex h-12 w-full rounded-2xl border border-white/10 bg-white/5 px-4 text-sm text-tuki-cream placeholder:text-tuki-cream/40",
          "font-body transition-colors focus-visible:outline-none focus-visible:ring-2 focus-visible:ring-tuki-lime focus-visible:ring-offset-2 focus-visible:ring-offset-tuki-night",
          "disabled:cursor-not-allowed disabled:opacity-50",
          className
        )}
        ref={ref}
        {...props}
      />
    );
  }
);
Input.displayName = "Input";

export { Input };
